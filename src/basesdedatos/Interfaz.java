    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {                                         
            Datos con = new Datos();
            Connection asd = con.conectar();
            
            
            String id, nom, ape, edad, tel, prof;
            String sql;
            
            id=jTextField7.getText();
            nom=jTextField1.getText();
            ape=jTextField2.getText();
            edad=jTextField3.getText();
            tel=jTextField4.getText();
            prof=jTextField5.getText();
            
            sql= "INSERT INTO datos (Id, Nombre, Apellido, Edad, Telefono, Profesion)VALUES (?,?,?,?,?, ?)";
            try {
                PreparedStatement pst= asd.prepareStatement(sql);
                pst.setString(1, id);
                pst.setString(2, nom);
                pst.setString(3, ape);
                pst.setString(4, edad);
                pst.setString(5, tel);
                pst.setString(6, prof);
                int x=pst.executeUpdate();
                if (x>0)
                    JOptionPane.showMessageDialog(rootPane, "Datos Registrados");
                
            } catch (SQLException ex) {
                Logger.getLogger(Interfaz.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Interfaz.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

